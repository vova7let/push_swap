#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tmp;

	if (lst && f)
	{
		while (lst)
		{
			tmp = lst;
			f(tmp);
			lst = lst->next;
		}
	}
}
